import socket

s = socket.socket(socket.AF_PACKET, socket.SOCK_RAW)
s.bind(("tun_srssl_m", 0))

ip_header  = b'\x45\x00\x00\x3c'  # Version, IHL, Type of Service | Total Length
ip_header += b'\xab\xcd\x00\x00'  # Identification | Flags, Fragment Offset
ip_header += b'\x40\x06\xa6\xec'  # TTL, Protocol | Header Checksum
ip_header += b'\x0a\x00\x02\x0b'  # Source Address
ip_header += b'\x0a\x00\x02\x0c'  # Destination Address

ip_content = b'\x00\x01\x02\x03'
ip_content = b'\x01\x00\x29\x01\x20\x51\x01\x00\x04\xa6\x01\x00\x1c\xe3\x00\x00\xbc\x00\x04\xe5\x48\x10\x99\xa4\xf1\xe2\x39\x20\x1f\x54\x9c\x1f\x07\xf1\xa3\xfe\x80\x02\x02\x9c'

packet = ip_header + ip_content
s.send(packet)

